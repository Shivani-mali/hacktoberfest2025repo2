   function showCategory(category) {
     const blogList = document.getElementById('blog-list');
     blogList.innerHTML = `<h2>${category.replace(/^\w/, c => c.toUpperCase())} Blogs</h2>`;
     
     blogs[category].forEach((blog, index) => {
       blogList.innerHTML += `<button class="blog-button" onclick="showBlog('${category}', ${index})">${blog.title}</button>`;
     });
   
     document.getElementById('blog-content').innerHTML = "";
   }
   
   function showBlog(category, index) {
     const blog = blogs[category][index];
     document.getElementById('blog-content').innerHTML = `
       <h2>${blog.title}</h2>
       <p>${blog.content}</p>
     `;
   }
   
 